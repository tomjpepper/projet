								<h2>Développement</h2>

								<p>Produite par Warner Bros. et Chuck Lorre Productions, la série a reçu le feu vert pour la production de treize épisodes le 14 mai 2007. Quatre épisodes supplémentaires viendront s'ajouter après l'importante grève des scénaristes qui a eu un impact sur la saison télévisuelle américaine en 2007-2008. Le 15 février 2008, CBS reconduit la série pour une seconde saison, du fait de ses audiences correctes pour une première saison.
								Le 12 janvier 2011, satisfaite des bonnes audiences, CBS a renouvelé la série pour trois saisons supplémentaires (cinq, six et sept), jusqu'à la saison 2013-20143.
								Le 12 mars 2014, de nouveau satisfaite des bonnes audiences, CBS a renouvelé la série pour trois saisons supplémentaires (amenant le total à 10 saisons), jusqu'à la saison 2016-20174.
								En raison d'un conflit sur les salaires des acteurs principaux (Jim Parsons, Johnny Galecki, Kaley Cuoco, Simon Heldberg et Kunal Nayyar), la production de la huitième saison qui devait débuter le 30 juillet 2014 a été reportée</p>.
								<img class="tournage" alt="tournage" src="image/14.jpg" />
								<h2>Tournage</h2>
								<p>La série est tournée dans les studios de Warner Bros. à Burbank en Californie, aux États-Unis.
								À la fin du dernier épisode de la cinquième saison, les héros sont tous sur le toit de leur immeuble, et une vue situe ce dernier aux coordonnées suivantes : +34° 8' 31.75"N, -118° 8' 18.70"O6
								Sur le plateau de tournage de la sixième saison, l'équipe et les acteurs de la série ont organisé un flashmob sur la chanson « Call Me Maybe » de Carly Rae Jepsen.</p>
								<h2>Générique d'ouverture</h2>
								<p>Le générique de début présente la formation de l'univers à partir du Big Bang. Il est donc possible de voir se succéder la naissance de l'univers, l'évolution de l'espèce humaine puis des innovations technologiques qui conduisent jusqu'à l'appartement des jeunes surdoués, où les cinq personnages mangent ensemble (de la cuisine chinoise dans la première saison, une pizza de la deuxième à la quatrième saison, de la cuisine thaï dans la cinquième saison). À partir de la sixième saison, Bernadette et Amy sont présentes dans le générique.
								La bande son s'intitule History of Everything8 et est interprétée par le groupe canadien Barenaked Ladies. Le 30 octobre 2007, une version longue du morceau (1 minute et 45 secondes) est sortie.</p>

