						<h3>Les Lieux d'action</h3>

							<h4>L’appartement de Leonard et Sheldon</h4> 
							<p>La plupart des scènes de la série se déroulent dans le salon de l’appartement de Leonard et Sheldon. Face au spectateur se trouve le canapé avec la télévision. Sur la droite, on trouve la cuisine et un couloir qui mène aux chambres et à la salle de bain. Sur la gauche se trouve la porte d’entrée.</p> 
							<h4>Le toit de l'immeuble </h4> 
							<p>Une scène s'y déroule dans l'épisode 23 de la saison 3. Il ré-apparait au dernier épisode de la saison 5 pour le mariage d'Howard et Bernadette.</p>
							<h4>L’appartement de Penny </h4>
							Il apparaît dans plusieurs épisodes. Lui aussi a pour pièce principale le salon avec un canapé. La chambre, la salle de bain et la porte d'entrée se trouvent sur la droite, la cuisine à gauche.</p> 
							<h4>The Cheesecake Factory </h4> 
							<p>L'endroit où travaille Penny et où la bande d'amis a décidé de venir manger régulièrement (à la suite d'une décision de Sheldon, car il est le seul à approuver ou désapprouver un restaurant comme étant digne d'y manger hebdomadairement).</p> 
							<h4>Le hall de l'immeuble </h4> 
							<p>Les personnages s'y rencontrent de temps en temps. C'est là qu'ils viennent récupérer leur courrier (c'est notamment là que Leonard vole le courrier de Penny afin de lui apporter ensuite, durant la première saison).</p> 
							<h4>L'université </h4> 
							Le lieu de travail de Leonard, Sheldon, Howard, Rajesh et Leslie. Certaines scènes se déroulent dans leurs bureaux, leurs laboratoires, mais surtout la cafétéria.</p> 
							<img class="fac" alt="fac" src="image/15.jpg" />
							<p>Certaines scènes sont filmées sur les paliers des étages de l'immeuble, et notamment celui des appartements de Leonard, Sheldon et Penny, au 4e étage. L'ascenseur est hors service depuis le début de la série à la suite d'une explosion issue d'une expérience ratée de Leonard au début de la colocation.</p> 
							<h4>La laverie de l'immeuble </h4> 
							<p>Située au sous-sol de l'immeuble de Sheldon, Leonard et Penny, elle apparaît dans la saison 2 et de nombreuses scènes s'y déroulent à partir de l'épisode 1.</p> 
							<h4>La maison des Wolowitz </h4> 
							<p>Il n'est pas rare de voir un des personnages sonner à la porte, ou encore d'admirer la chambre très kitsch d'Howard en entendant les hurlements de sa mère. Avec le porche de l'entrée et le salon, ce sont les seules pièces vues dans la série. Et on voit dans le dernier épisode de la quatrième saison, ce qui semble être le salon, avec un canapé, et aussi une petite partie où se trouvent les toilettes.</p> 
							<h4>La boutique de comics </h4> 
							<img class="comic" alt="comic" src="image/13.jpg" />
							La fameuse boutique où Sheldon veut toujours aller, régulièrement des scènes s'y déroulent. Il est par contre l'endroit central de l'épisode 5 de la troisième saison. La boutique est tenue par Stuart.</p> 
							<h4>L'appartement de Raj </h4> 
							Peu d'épisodes s'y déroulent. Cependant, il apparaît plusieurs fois dans la quatrième saison et presque tout un épisode a été réalisé à l'intérieur.</p> 
							<h4>L'appartement d'Amy </h4> 
							<p>Quelques épisodes s'y déroulent. Il apparaît pour la première fois dans la quatrième saison.</p> 
							<h4>L'appartement de Bernadette </h4> 
							<p>Il apparaît une fois dans la cinquième saison où ne s'y déroule qu'une seule scène. Il est plus souvent présent à partir de la sixième saison.</p> 
							<h4>L'appartement du père de Bernadette </h4> 
							<p>Il apparaît une fois dans la cinquième saison. Il ne s'y déroule qu'une seule scène. C'est d'ailleurs ici qu'apparaît pour la première fois le père de Bernadette.</p> 
							<h4>La cabane de Paintball </h4> 
							<p>Apparaît quelquefois quand Leonard, Sheldon, Howard et Raj vont faire une partie de paintball.</p>
																
						
							
