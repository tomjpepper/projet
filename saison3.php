						<h3>Episode 1 :</h3>  

						<p>Après trois mois d'expédition au pôle Nord, Howard, Raj et Leonard sont de retour. Penny saute sur Leonard, l'embrassant à pleine bouche. Quant à Sheldon, il est persuadé de recevoir le prix Nobel, car, en Arctique, il a réussi une expérience révolutionnaire. Ses amis lui avouent qu'ils l'ont en fait truquée en se servant d'un ouvre-boîte électrique afin d'obtenir le résultat escompté ! Leur intention était d'éviter un nouvel échec qui l'aurait rendu invivable. Terriblement déçu et déprimé, Sheldon présente sa démission à l'Université et repart vivre chez sa mère, au Texas. Howard, Raj et Leonard s'y rendent, espérant le ramener en Californie...</p>
						<h3>Episode 2 :  </h3>

						<p>Leonard raconte à ses amis sa première nuit avec Penny et la qualifie de simplement «sympathique». Sheldon ne peut s'empêcher d'en parler à Penny. Leonard et Penny tentent alors de comprendre pourquoi leurs relations intimes n'atteignent pas ce qu'ils pourraient en espérer. Pendant ce temps, Sheldon et Howard se lancent dans un pari à propos de l'identité de l'insecte qu'ils ont trouvé dans l'immeuble : un grillon domestique ou un Oecanthus fultoni ? Celui qui aura raison remportera un précieux exemplaire de bande dessinée...</p>
						<h3>Episode 3 :  </h3>

						<p>Sheldon tente de faire retrouver à Penny sa bonne humeur avec des chocolats, en espérant qu'elle sera plus gentille avec lui. Raj et Howard décident de s'habiller «gothique» pour aller draguer dans une discothèque...</p>
						<h3>Episode 4 :  </h3>

						<p>Raj, dont les recherches entamées depuis six mois n'ont rien donné, annonce qu'il sera prochainement renvoyé dans son pays s'il ne trouve pas un nouveau travail. Malgré sa réticence, il accepte de travailler pour Sheldon. Penny et Leonard espèrent pouvoir ainsi avoir un peu plus de temps pour eux. C'était compter sans Howard, qui devient de plus en plus envahissant...</p>
						<h3>Episode 5 :  </h3>

						<p>Howard rappelle à Leonard le pacte qu'ils avaient passé quelques années auparavant : le premier à avoir une petite amie devait encourager celle-ci à présenter une de ses amies à l'autre. Leonard demande donc à Penny d'organiser un rendez-vous entre Howard et une de ses amies. De son côté, Raj tente de convaincre Sheldon de participer à un tournoi de cartes. Celui-ci finit par accepter lorsqu'il lui apprend que Wil Wheaton, son ennemi de toujours, sera de la partie...</p>
						<h3>Episode 6 :  </h3>

						<p>Penny organise une soirée football avec ses copains, sans inviter Leonard, sous prétexte qu'il n'aime pas ce sport. Leonard la soupçonne de ne pas vouloir le présenter à ses amis. Elle l'invite alors au prochain match, alors qu'il n'y connaît absolument rien en football. Parallèlement, un incident de cerf-volant met à mal l'amitié de Raj et de Howard...</p>
						<h3>Episode 7 :  </h3>

						<p>Penny annonce à Leonard que Justin, l'un de ses amis guitaristes et ancien amant, vient dormir chez elle pendant quelques jours. Celui-ci prend mal la nouvelle et lui fait part de son mécontentement. Penny et Leonard se disputent sans cesse. Sheldon se sent très mal à l'aise face à cette situation et pense que leur relation touche à sa fin...</p>
						<h3>Episode 8 :  </h3>

						<p>Leonard, Raj et Howard partent faire du camping nocturne afin d'observer une pluie de météorites. Là, deux hippies leur proposent des cookies. Mais ceux-ci contiennent une substance un peu spéciale. Leonard, Raj et Howard passent leur nuit à divaguer, à rire bêtement, et passent finalement à côté de l'événement astronomique pour lequel ils étaient venus. De son côté, Sheldon se voit obligé de s'occuper de Penny qui vient de se déboîter l'épaule alors qu'elle prenait un bain...</p>
						<h3>Episode 9 :  </h3>

						<p>Sheldon annonce fièrement à ses amis qu'il va être interviewé par la radio nationale afin de parler de ses recherches. Mais, lorsque Kripke, un scientifique qui le déteste, lui tend un piège et le ridiculise, Sheldon refuse de sortir de sa chambre, humilié. Leonard lui propose alors de se venger. De son côté, Howard, qui en est à son troisième rendez-vous avec Bernadette, ne sait pas s'il tient à s'engager dans une relation sérieuse avec elle..</p>
						<h3>Episode 10 :  </h3>

						<p>Bernadette s'intéresse au travail de Howard, son fiancé, ainsi qu'à celui de Leonard. Elle veut même assister au lancement de l'accélérateur d'électrons mis au point par Leonard, ce qui rend fou de jalousie Howard, lequel regrette déjà d'avoir présenté Bernadette à ses amis. C'est alors que Penny demande à Sheldon de lui enseigner les bases de la physique afin qu'elle puisse elle-aussi prendre part aux travaux de Leonard....</p>
