							<h3>Episode 2 : </h3>
							<p>Penny est furieuse : Leonard et Sheldon se sont introduits dans son appartement pendant la nuit. Les deux amis ont passé des heures à ranger et nettoyer les lieux alors que la jeune femme dormait...</p>
							<h3>Episode 3 :  </h3>
							<p>Quand Leonard voit Penny, la voisine dont il est amoureux, embrasser un homme devant sa porte, il est effondré. Il réalise alors combien il est loin de correspondre à l'idéal masculin de la jolie Penny. Cependant, encouragé par ses amis et collègues scientifiques, il décide de tenter sa chance avec une autre jeune femme. Il invite Leslie, une de ses collègues, à sortir avec lui. Celle-ci ayant refusé assez vertement, Leonard se sent encore plus déprimé. Poussé par Sheldon, qui ne croit pas un instant à la possibilité d'une liaison entre son ami et leur voisine, il reprend courage et décide d'inviter Penny. Mais celle-ci interprète mal sa proposition...</p>
							<h3>Episode 4 : </h3>
							<p>Après avoir été licencié, Sheldon découvre que la vie ne se résume pas à la physique. Leonard décide de prendre des mesures drastiques pour sortir son ami de ce qu'il pense être une impasse. Il appelle Mary, la mère de Sheldon, à la rescousse...</p>
							<h3>Episode 5 : </h3>
							<p>Voyant que ses démarches auprès de Penny ne mènent à rien, Leonard accepte une invitation de sa collègue Leslie Winkle. Celle-ci le séduit et les deux jeunes gens passent la nuit ensemble. Cependant Leslie est très claire : elle n'a aucune intention de s'engager dans une relation sérieuse. Dépité, Leonard cherche conseil auprès de Penny. La jeune femme a du mal à cacher sa jalousie jusqu'à ce qu'elle réalise que Leslie et Leonard ne sortent pas réellement ensemble. De son côté, Sheldon prend une décision capitale, après moults hésitations : dorénavant il mangera son hamburger quotidien dans un autre restaurant...</p>
							<h3>Episode 6 : </h3>

							<p>Leonard, Sheldon et leurs amis se rendent à la fête d'Halloween organisée par Penny. Sheldon, Wolowitz et Koothrappali tentent désespérément de séduire les jeunes femmes présentes...</p>
							<h3>Episode 7 : </h3>

							<p>Wolowitz séduit l'amie de Penny et fait l'amour avec elle dans l'appartement de Penny. Celle-ci va donc dormir sur le divan de Sheldon et Léonard. Pendant ce temps, les garçons se rendent compte qu'en l'absence de Wolowitz, il manque une quatrième personne dans leur équipe. Penny est donc recrutée...</p>
							<h3>Episode 8 : </h3>

							<p>Les parents de Koothrappali lui ont trouvé une fiancée. Mais Raj a beaucoup de mal à parler aux femmes. Il découvre finalement le moyen de maîtriser sa peur. C'est à ce moment que la jeune femme montre de l'intérêt pour Sheldon...</p>

							<h3>Episode 9 : </h3>

							<p>Sheldon et Leonard sont invités à présenter leurs travaux lors d'une conférence sur la physique. Cette invitation est source de dispute pour les deux amis, car Sheldon refuse de partager les honneurs. Penny fait de son mieux pour les réconcilier, mais cela n'arrange rien...</p>
							<h3>Episode 10 :</h3>
							<p>Penny invite Leonard et Sheldon à assister à un spectacle dans lequel elle se produit. Mais après l'avoir entendue chanter, les deux amis tentent de se défiler. Leonard se contente d'un mensonge simple et crédible tandis que Sheldon n'hésite pas à s'inventer un cousin imaginaire...</p>
